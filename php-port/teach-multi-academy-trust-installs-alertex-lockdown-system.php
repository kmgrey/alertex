                            <p>
                                Luminite supplied a mixture of internal and external units meaning that no matter where the initial alert point was triggered and 
                                due to ‘flood technology’, all pupils and staff both inside and out would hear the voice alert and see the flashing blue beacons. 
                                ALERTEX lockdown units are deliberately blue in colour to differentiate them from fire beacons and there are 32 sounds to choose 
                                from to further differentiate them from fire alarms.  The fact that the ALERTEX system could be installed by the Trust’s own Site 
                                Managers was a significant advantage, as their installation is quick and easy requiring no specialist or technical skills.
                                <br><br>
                                The TEACH Trust H&S Coordinator said “The ALERTEX system was installed without any disruption to the school day by the Site 
                                teams, following simple instructions supplied by Luminite who were available throughout for back-up support. After fully 
                                preparing our pupils for the event, we successfully completed lockdown drills without causing any distress to pupils or staff. 
                                There are no ongoing maintenance costs, other than a 3-yearly change of batteries and we would recommend the system to other educational establishments”. 
                            </p>

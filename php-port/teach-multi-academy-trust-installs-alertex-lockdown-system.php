                                Training Centre, which trains up to 30 primary school teachers per year situated in Canford Heath, a suburban area of Poole, 
                                Dorset.  Almost 1500 pupils, from ages 4yrs to 11yrs attend across the four schools.
                                <br><br>
                                The Trust’s Health and Safety Coordinator faced a challenging task to provide an effective lockdown alert system for each school. 
                                Luminite’s ALERTEX critical alert range was chosen as it could provide a wireless solution, negating the need for a hard-wired 
                                system which would have been more costly in terms of resources and installation.  

                    <p class="p-head white-text">
                        All schools must have a <span class="yellow-text">Lockdown policy</span> in place that outlines their action plan for the restriction of movement during an emergency situation. 
                        A lockdown policy aims to prevent people from freely entering, leaving, or moving around an area to minimise disruption and danger.
                        <br><br>
                        In line with OFSTED guidance the <span class="yellow-text">Lockdown system</span> should provide an audible and/or visual alert that is specific to a scenario requiring free movement to be restricted. The Lockdown system should look and sound different to a fire alarm, and should provide a clear alert swiftly and reliably.
                    </p>

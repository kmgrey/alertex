            <div class="outer-container">
                <div class="inner-container flex-column">
                    <h2>Why do you need a Lockdown system?</h2>
                    <p>
                        An emergency Lockdown System is now an essential part of any preparedness plan for institutions with a duty of care to students, staff, and the general public. 
                        As a Lockdown scenario requires a different response to a Fire scenario, using a dedicated Lockdown System is preferred, as outlined in 
                        the <a class="p-link" href="https://www.protectuk.police.uk/evacuation-invacuation-lockdown-protected-spaces" target="_blank">‘Evacuation, 
                        invacuation, lockdown, protected spaces’ guidance</a> provided by NaCTSO:
                    </p>
                    <p class="quote">
                        “In some emergency scenarios such as a terrorist attack, the appropriate response may be not to evacuate. Where evacuation may be required, 
                        the evacuation response may differ to that of a fire. For example, people may be directed to specific exits or to avoid a particular route or area.
                        <br><br>
                        For this reason, it is suggested the activation of the fire alarm to initiate evacuation should be avoided to reduce the possibility of an 
                        incorrect response. Public Address (Tannoy) systems, if available, may provide more flexibility to provide information and instructions 
                        appropriate to the scenario and to provide positive confirmation to staff and visitors that the emergency is real. 
                        This will help to reduce any potential delay in response.”
                    </p>

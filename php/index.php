                        <h2>How does the Alertex Wireless Lockdown System work?</h2>
                        <p>
                            The Alertex Wireless Critical Alert System forms an interconnecting wireless mesh network between all units on the same site code. When one unit is activated, 
                            it will activate all other units on the same site. The maximum line of sight wireless range for Alertex units is 1km, and units can be set to repeat the signal, 
                            which in turn extends the wireless transmission range.
                            <br><br>
                            The Alertex system can be activated by any call-point or key fob within range of a main unit, or remotely from a central location using the Alertex web portal and IP bridge.
                            <br><br>
                            The system is fully standalone and requires no wires or cabling. Units are battery-powered with a long battery-life of up to 2-years.
                        </p>

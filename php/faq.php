                <h2>Alertex Units</h2>
                <div class="outer-container divider flex-wrap">
                    <div class="inner-container flex-column">
                        <p>
                            <strong>Q: <em>How long do the batteries last?</em></strong>
                            <br>
                            <strong>A: </strong>The battery life of the Lithium D cells is around 2 years with normal use. We recommend changing the 
                            batteries every 2 years during maintenance. If the system includes an Alertex IP Bridge, the battery status of 
                            the Alertex units will be reported.
                        </p>
                        <p>
                            <strong>Q: <em>How do I perform a silent test using a master unit?</em></strong>
                            <br>
                            <strong>A: </strong>On the master unit, turn the reset key on the side of the unit to the ‘on’ position. 
                            After 10 seconds, all the units within range on the same site will begin to flash. To stop the test, 
                            turn the reset key back to the ‘off’ position.
                        </p>
                        <p>
                            <strong>Q: <em>The system is not being activated by my wireless call-point or key fob.</em></strong>
                            <br>
                            <strong>A: </strong>Check that the units are set to the same site and subnet code, and that each unit has a unique unit number. 
                            Please note that due to the nature of the wireless protocol, the system requires a 4-5 minute rest period after an 
                            activation to settle before the system can be activated again.
                        </p>
                        <p>
                            <strong>Q: <em>How do I programme my key fob or wireless call point?</em></strong>
                            <br>
                            <strong>A: </strong>Please refer to the <a href="./instructions/nxfob-instructions.pdf" target="_blank">Alertex Wireless Key Fob instructions</a> to programme the wireless key fob. 
                            <br>
                            <strong>A: </strong>Please refer to the <a href="./instructions/nxicp1-instructions.pdf" target="_blank">Alertex Wireless Call-Point instructions</a> to programme the wireless call-point.                            
                        </p>
                        <p>
                            <strong>Q: <em>How should I position my Alertex unit?</em></strong>
                            <br>
                            <strong>A: </strong>As the Alertex units transmit using a UHF signal, positioning them too close to a ceiling or corner wall can interfere with the wireless transmission. 
                            You should position the unit at least a metre away from a ceiling or corner wall. If you want to position the unit close to the ceiling, ensure that 
                            the unit is fixed so that the antenna points downwards.
                        </p>
                        <p>
                            <strong>Q: <em>Why isn't my unit turning on?</em></strong>
                            <br>
                            <strong>A: </strong>Firstly, check that the batteries are not flat. Verify that each unit is programmed with the same site and subnet code, and that each unit has a 
                            unique unit number. If unit numbers are repeated in the same system, the unit may not activate.
                        </p>
                    </div>
                    <div class="inner-container flex-column">
                        <p>
                            <strong>Q: <em>There is no power indication on my unit.</em></strong>
                            <br>
                            <strong>A: </strong>Please check that there is no water ingress, that the fuse is in place 
                            and functional, and that the blue jumper is properly positioned and has not dislodged.
                        </p>
                        <p>
                            <strong>Q: <em>How do I add more Alertex units to the system? </em></strong>
                            <br>
                            <strong>A: </strong>Additional units can be added to the Alertex system by programming the same site and subnet 
                            code onto the new unit, and giving it a unique unit number. You can have up to 64 Alertex units on one system.
                        </p>
                        <p>
                            <strong>Q: <em>What is the maximum volume of the sounder?</em></strong>
                            <br>
                            <strong>A: </strong>The maximum volume of Alertex sounders is 117 dB, and can be adjusted using the dip 
                            switches on the back of the sounder (please see instructions for details).
                        </p>
                        <p>
                            <strong>Q: <em>What is the maximum volume of the voice annunciator?</em></strong>
                            <br>
                            <strong>A: </strong>The maximum volume of the Alertex voice annunciator is 105 dB, but this is dependent 
                            on the original volume of the audio file.
                        </p>
                        <p>
                            <strong>Q: <em>Can I upload my own message to the annunciator?</em></strong>
                            <br>
                            <strong>A: </strong>Custom messages can only be uploaded during the programming of the unit. 
                            Please send  your audio files to us prior to dispatch.
                        </p>
                    </div>

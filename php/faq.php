                <h2>Alertex IP Bridge</h2>
                <div class="outer-container divider flex-wrap">
                    <div class="inner-container flex-column">
                        <p>
                            <strong>Q: <em>What do I need to set-up the Alertex IP Bridge?</em></strong>
                            <br>
                            <strong>A: </strong>To set up an Alertex IP Bridge, you need an internet connection, a 5-volt or 12-volt power source, and an Android or iOS smartphone.
                        </p>
                        <p>
                            <strong>Q: <em>Why does the Alertex IP Bridge say ‘No IP yet’?</em></strong>
                            <br>
                            <strong>A: </strong>Make sure that the network you are connected to has internet access in order to obtain the IP address.
                        </p>
                        <p>
                            <strong>Q: <em>What is the default BLE pin?</em></strong>
                            <br>
                            <strong>A: </strong>123456
                        </p>
                        <p>
                            <strong>Q: <em>What is the default portal password?</em></strong>
                            <br>
                            <strong>A: </strong>password1234
                        </p>
                        <p>
                            <strong>Q: <em>Is the Alertex IP Bridge waterproof?</em></strong>
                            <br>
                            <strong>A: </strong>The Alertex IP Bridge is IP66 weather rated.
                        </p>
                        <p>
                            <strong>Q: <em>Where should I install the Alertex IP Bridge?</em></strong>
                            <br>
                            <strong>A: </strong>The Alertex IP Bridge should be fitted in a central location, preferably in the middle of the site, so that the 
                            Alertex units around the site are within range and spaced evenly.
                        </p>
                        <p>
                            <strong>Q: <em>What is the Alertex IP Bridge's default network configuration?</em></strong>
                            <br>
                            <strong>A: </strong>The Alertex IP Bridge is configured using the DHCP protocol by default. 
                            You can set a static IP address using the BLE console before connecting.
                        </p>
                        <p>
                            <strong>Q: <em>I have an internet connection but am unable to connect to the Alertex IP Bridge.</em></strong>
                            <br>
                            <strong>A: </strong>Check that the Alertex IP Bridge and your PC are connected to the same network via ethernet or Wi-Fi. The Alertex IP Bridge cannot be accessed by a PC connected to a different network.
                        </p>
                    </div>
                    <div class="inner-container flex-column">
                        <p>
                            <strong>Q: <em>The battery status displayed on the Alertex portal is wrong or not displayed at all.</em></strong>
                            <br>
                            <strong>A: </strong>Do a site-wide silent test using a master unit, and allow the units to flash for at least a minute before ending the test. 
                            Once the test has concluded, the units should call in and update their battery status.
                        </p>
                        <p>
                            <strong>Q: <em>How many email addresses can I add in the Alertex portal?</em></strong>
                            <br>
                            <strong>A: </strong>You can add an unlimited number of email addresses into the portal, however, it is good practice to only inform key staff of important 
                            events such as activations, missed calls, or low battery notifications.
                        </p>
                        <p>
                            <strong>Q: <em>How do I change my BLE pin and Alertex portal password?</em></strong>
                            <br>
                            <strong>A: </strong>You can change the BLE pin and Alertex portal password in the ‘Settings’ section of the Alertex portal. Please be advised that it is 
                            exclusively the customer's duty to remember the password and pin as it is impossible to reset the password without reloading the Alertex IP Bridge firmware to the unit.
                        </p>
                        <p>
                            <strong>Q: <em>What happens if I lose or forget my BLE pin or Alertex portal password?</em></strong>
                            <br>
                            <strong>A: </strong>If you lose or forget your BLE pin or Alertex portal password, the Alertex IP Bridge’s firmware must be reloaded. 
                            Please call our support team on 0208 368 7887 for assistance.
                        </p>
                        <p>
                            <strong>Q: <em>How do I reset the Alertex IP Bridge?</em></strong>
                            <br>
                            <strong>A: </strong>Go to the ‘Settings’ section of the Alertex portal. At the bottom of the page, click ‘Factory reset’. 
                            This will reset all the settings stored on the Alertex IP Bridge to factory settings.
                        </p>
                        <p>
                            <strong>Q: <em>What is a call-period (CP)?</em></strong>
                            <br>
                            <strong>A: </strong>The call-period is the interval at which Alertex units connect to the Alertex IP Bridge to report their signal strength and battery status. The default call-period for both 
                            Alertex units and the Alertex IP Bridge is set to 60. Please see the <a href="./instructions/alertex-instructions-booklet.pdf">Alertex instructions</a> for details on call-period intervals.
                        </p>
                        <p>
                            <strong>Q: <em>How do I set the audio file that plays on my Voice Annunciator from the Alertex portal?</em></strong>
                            <br>
                            <strong>A: </strong>Go to the ‘Playback Controls’ section of the Alertex portal. In the ‘Menu parameters’ section, set which unit you wish to configure (individual units or all units), 
                            set which parameters you want the audio file to play on, and then set the value (index of the audio file) to select which audio file will play. 
                            Please refer to the <a href="./instructions/nxipbridge-instructions-booklet-nov23.pdf" target="_blank">Alertex IP Bridge instructions</a> 
                            or the <a href="https://youtu.be/Zf_wz1HA0Bg?si=Nmr78LDFZxlZPZcY&t=244" target="_blank">Alertex web portal overview</a> for more information.
                        </p>
